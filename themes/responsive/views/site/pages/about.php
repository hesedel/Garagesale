<h1>About</h1>

<p>Stycle is a student community space to buy and sell second hand items. It is also a space for community shoutouts and for students to find out about events happening locally.</p>

<p>The best thing about Stycle is it is free to use! Free to buy and sell! Free to put up a shout out! And you can do all this from your mobile!</p>

<p>Stycle was founded in May 2014 by a team of University students, Adam, Amy, Hes and Marsha. Their goal was to create a local space for students to make some cash through selling items and also buy items they need for their course, but it evolved as they found out that students needs come in many shapes and forms.</p>

<p>Since it's first sale, Stycle has become a student space to sell anything from course materials, to furniture, to material, to computers, to baby clothes, to last night's fancy dress costume! What will you find on the Stycle site?</p>

